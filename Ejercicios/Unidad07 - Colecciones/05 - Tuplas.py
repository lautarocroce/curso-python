#Las tuplas son inmutables. Quiere decir que no se puede modificar, agregar o eliminar
#los datos agregados.

frutas = ('Naranja','Platano','Guayaba')
print(frutas)
#saber el largo
print(len(frutas))
#acceder a un elemento
print(frutas[0])
#navegacion inversa
print(frutas[-1])
#acceder a un rango
print(frutas[0:2]) #Sin incluir el último indice


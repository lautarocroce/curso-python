#Asignación
miVariable = 10
print(miVariable)
#Incremento con reasignación
miVariable += 2
print(miVariable)
#Disminución con reasignacion
miVariable -= 2
print(miVariable)
#Multiplicacion con reasignacion
miVariable *= 3
print(miVariable)
#Division con reasignacion
miVariable /= 3
print(miVariable)
a = 4
b = 2
# = asignacion == comparacion
resultado = a == b
print(f'Resaultado == : {resultado}')
# != diferente
resultado = a != b
print(f'Resaultado != : {resultado}')
# > mayor que
resultado = a > b
print(f'Resaultado > : {resultado}')
# >= mayor o igual que
resultado = a >= b
print(f'Resaultado >= : {resultado}')

